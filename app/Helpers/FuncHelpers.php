function cate_parent($data, $parent = 0, $str = "+", $select = 0)
{
    foreach ($data as $val) {

        if ($val['parent_id'] == $parent) {
            if ($select != 0 && $val['id'] == $select) {
                echo "<option value='" . $val['id'] . "' selected='selected'>" . $str . ' ' . $val['name'] . "</option>";
            } else {
                echo "<option value='" . $val['id'] . "'>" . $str . ' ' . $val['name'] . "</option>";
            }

            cate_parent($data, $val['id'], "--");
        }
    }
}

function cate_select2($data, $parent = 0)
{
    foreach ($data as $val) {
        if ($val['parent_id'] == $parent) {
            if ($val['parent_id'] == 0) {
                echo "<optgroup label=\"" . $val->translate('vi')->name . "\"></optgroup>";
            }
            cate_parent($data, $val['id'], "");
        }
    }
}


function generate_string($strength = 16)
{
    $permitted_chars = '0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ';
    $input_length = strlen($permitted_chars);
    $random_string = '';
    for ($i = 0; $i < $strength; $i++) {
        $random_character = $permitted_chars[mt_rand(0, $input_length - 1)];
        $random_string .= $random_character;
    }
    return $random_string;
}

